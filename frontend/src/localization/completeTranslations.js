// Translation helper function
export const useGlobalTranslation = (language = 'en') => {
  const t = (category, key, fallback = '') => {
    const translation = globalTranslations[language]?.[category]?.[key] 
      || globalTranslations['en']?.[category]?.[key] 
      || fallback 
      || key;
    
    return translation;
  };

  const tc = (key, fallback = '') => {
    return t('common', key, fallback);
  };

  const tn = (key, fallback = '') => {
    return t('navigation', key, fallback);
  };

  const tf = (key, fallback = '') => {
    return t('forms', key, fallback);
  };

  const tm = (key, fallback = '') => {
    return t('messages', key, fallback);
  };

  return { t, tc, tn, tf, tm };
};

// Error boundary component for translation fallback
export const GlobalError = ({ children }) => {
  return children;
};

export default globalTranslations;